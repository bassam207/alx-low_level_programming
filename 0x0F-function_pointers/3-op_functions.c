#include "3-calc.h"

/**
* op_add - adds two integers
* @a: the first int
* @b: the second int
* Return: the sum
*/
int op_add(int a, int b)
{
	return (a + b);
}

/**
* op_sub - subtracts two ints
* @a: the first int
* @b: the second int
* Return: the int difference
*/
int op_sub(int a, int b)
{
	return (a - b);
}

/**
* op_mul - multiplies two ints
* @a: the first int
* @b: the second int
* Return: the int product
*/
int op_mul(int a, int b)
{
	return (a * b);
}

/**
* op_div - divides two ints
* @a: the first int
* @b: the second int
* Return: the int quotient
*/
int op_div(int a, int b)
{
	return (a / b);
}

/**
* op_mod - module two ints
* @a: the first int
* @b: the second int
* return: the int remainder
*/
int op_mod(int a, int b)
{
	return (a % b);
}
